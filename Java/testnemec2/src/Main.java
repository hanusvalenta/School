public class Main {
    public static void main(String[] args)
    {
        int a = 10;
        switch(a){
            case 10:
                System.out.println("10");
                break;
            case 11:
                System.out.println("11");
                break;
        }

        while (a > 10){
            System.out.println(a);
        }
    }
}